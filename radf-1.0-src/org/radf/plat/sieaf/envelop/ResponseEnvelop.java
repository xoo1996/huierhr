import java.io.Serializable;

public class ResponseEnvelop implements Serializable{

    private ResponseEnvelopHead head;
    private Object body;
    public ResponseEnvelop() {
    }
    public ResponseEnvelopHead getHead() {
        return head;
    }
    public void setHead(ResponseEnvelopHead head) {
        this.head = head;
    }
    public void setBody(Object body) {
        this.body = body;
    }
    public Object getBody() {
        return body;
    }
}